		<p> La compétition aura lieu le 25 juin 2024 à Trifoulli les oies. Il est composé de deux courses : l’ADO FUN et l’HARD RUN.</p>
		<ul>
			<li>ADO FUN : une course de 10 km sur route et forêt. Circuit facile avec moins de 100 m de Dénivelé positif : départ à 10h00 (un ravitaillement à mi-course)</li>
			<li>HARD ORUN : un trail de 35 km, uniquement en forêt. Circuit technique, nombreux singles et plus de 1000m de D+ : 
			départ à 8h30 (un ravitaillement au 15ème km et un ravitaillement au 25ème kilomètre (sur chaque ravitaillement, il y aura de l’eau, des fruits et des barres de céréales)</li>
		</ul>
		<p>Le retrait des dossards se fera sur place jusqu’à une heure avant le départ de la course, à la salle des fêtes de Trifoulli les oies. 
		Le départ sera donné en face de l'église. Le dossard doit être obligatoirement porté sur le devant et visible ceci pendant toute la durée de la course.</p>
		
		<h3>Article 3-1 : Condition d’inscription Catégories d’âge</h3>
		
			<ul>
				<li>Le 10 km (ADO FUN) est ouvert à partir de la catégorie « Cadets » (plus de 16 ans).</li>
				<li>Le 35 km (HARD RUN) est ouvert à partir de la catégorie « Espoirs » (plus de 20 ans).</li>
			</ul>

		<p>Les mineurs peuvent s’inscrire sur l’ADO FUN à condition qu’ils aient plus de 16 ans révolu le jour de la course. 
		Une autorisation parentale sera à fournir en pièce jointe sur le formulaire d’inscription ou présenter sur place.</p>
		
		<h3>Article 3-2 : Certificat médical et licences sportives</h3>
			<p>Conformément à l’article 231-2-1 du code du sport, la participation à la compétition est soumise à la présentation obligatoire :</p>
			<ul>
				<li>soit d’une licence sportive FFA, FFTRI, FFCO, FFPM ou UFOLEP Athlé (en cours de validité à la date de la manifestation).</li>
				<li>	soit d’un certificat médical de non contre-indication à la pratique de l’Athlétisme en compétition ou de la course à pied en compétition, 
				datant de moins de un an à la date de la compétition, ou de sa copie. Aucun autre document ne peut être accepté pour attester de la possession du certificat médical</li>
			</ul>
			
		<h3>Article 4 : Inscriptions</h3>
			<p> Les inscriptions se font uniquement en ligne via notre <a href="inscription.php">plateforme d’inscription</a>.<br />
