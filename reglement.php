			L’organisation ne peut en aucun cas être tenue pour responsable en cas d’accident ou de défaillance des participants 
			notamment ceux consécutifs à un mauvais état de santé ou à une préparation insuffisante. 
			La participation se fait sous l’entière responsabilité des concurrents avec renonciation à tout recours 
			contre les organisateurs en cas de dommages ou de séquelles ultérieurs à la course. 
			Les organisateurs déclinent toute responsabilité en cas de vol ou de dégradation de matériel.</p>
			
		<h3>Article 6 : Sécurité</h3>
			<p>La sécurité et l’assistance médicale des participants est du ressort de l’organisateur qui mettra en place un Dispositif Prévisionnel de Secours (DPS). 
