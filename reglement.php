		<h3>Article 4-2 : Revente ou cession de dossard</h3>
			<p>Tout engagement est personnel. Aucun transfert d’inscription n’est autorisé pour quelque motif que ce soit. 
			Toute personne rétrocédant son dossard à une tierce personne, sera reconnue responsable en cas d’accident 
			survenu ou provoqué par cette dernière durant l’épreuve. Toute personne disposant d’un dossard acquis en infraction 
			avec le présent règlement pourra être disqualifiée.<br />  
			L’organisation décline toute responsabilité en cas d’accident face à ce type de situation</p>
			
		<h3>Article 4-3 : Remboursement</h3>
			<p>Chaque engagement est ferme et définitif. Cependant, l’organisation accepte des changements de coureurs. 
			Il vous est donc possible de céder votre dossard à un remplaçant à condition que vous ayez contacté 
			l’organisateur pour communiquer le nom, prénom, date de naissance, certificat médical du remplaçant.</p>

