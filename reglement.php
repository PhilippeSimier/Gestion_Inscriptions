			<p>Le 36 km (HARD RUN) : les 3 premiers de la catégorie JEUNE (cadets, juniors, espoirs), SENIOR et MASTER (V1,V2,V3,V4), en homme et en femme</p>
			<p>Une pomme ainsi qu’une médaille en chocolat sera distribuée sur la ligne d’arrivée à chaque coureur terminant l’épreuve. <br />
